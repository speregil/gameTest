using UnityEngine;
using System.Collections;

public class SpawnAttackArea : MonoBehaviour {

//---------------------------------------------------------------------------------------------------
// Atributes
//---------------------------------------------------------------------------------------------------

//Editable Values
	public float maxTime;

//Private Values
	private float initialTime;

//---------------------------------------------------------------------------------------------------
// Initialization
//---------------------------------------------------------------------------------------------------

	void Start () {
		initialTime = Time.time;
	}

//---------------------------------------------------------------------------------------------------
// Behaviour
//---------------------------------------------------------------------------------------------------

	void Update () {
		if (Time.time >= initialTime + maxTime) {
			Destroy(this.gameObject);
		}
	}
}